# ── 1. MNA Solver Constants (Matching the generator exactly) ───────────────
FREQ_START_HZ = 40e6
FREQ_STOP_HZ  = 43.5e9
FREQ_STEP_HZ  = 100e6
FREQS         = np.arange(FREQ_START_HZ, FREQ_STOP_HZ, FREQ_STEP_HZ)
N_FREQ        = len(FREQS)
OMEGA         = 2.0 * np.pi * FREQS

Z0 = 50.0
RI_VAL   = 0.35
LPAD_VAL = 50e-12
CCT_VAL  = 5.5e-15
CPAD_VAL = 4.5e-15

_Y_RI   = 1.0 / RI_VAL
_Y_LPAD = 1.0 / (1j * OMEGA * LPAD_VAL)
_Y_CCT  = 1j * OMEGA * CCT_VAL
_Y_CPAD = 1j * OMEGA * CPAD_VAL

_PORT_IDX = [0, 5]
_INT_IDX  = [1, 2, 3, 4]
N_NODES = 6
_I2 = np.eye(2, dtype=complex)

def _stamp(Y, i, j, y):
    Y[:, i, i] += y
    Y[:, j, j] += y
    Y[:, i, j] -= y
    Y[:, j, i] -= y

def _stamp_gnd(Y, i, y):
    Y[:, i, i] += y

# ── 2. Vectorized Forward Model (MNA) ───────────────────────────────────────
def simulate_1740(theta):
    """
    theta order: [Rbv, Cbv, Rdv, Ldv_nH, Cdv, Rav]
    Returns 1740D flat array: [Re(S11), Im(S11), Re(S22), Im(S22)]
    """
    Rbv, Cbv_fF, Rdv, Ldv_nH, Cdv_fF, Rav = theta

    # Protect against optimizer passing zero/negative bounds infinitesimally
    Ldv = max(Ldv_nH * 1e-9, 1e-15)
    Cbv = max(Cbv_fF * 1e-15, 1e-18)
    Cdv = max(Cdv_fF * 1e-15, 1e-18)
    Rav = max(Rav, 1e-3)
    Rdv = max(Rdv, 1e-3)
    Rbv = max(Rbv, 1e-3)

    y_Rav = 1.0 / Rav
    y_Rdv = 1.0 / Rdv
    y_Rbv = 1.0 / Rbv
    y_Ldv = 1.0 / (1j * OMEGA * Ldv)
    y_Cbv = 1j * OMEGA * Cbv
    y_Cdv = 1j * OMEGA * Cdv

    Y = np.zeros((N_FREQ, N_NODES, N_NODES), dtype=complex)

    _stamp(Y, 0, 1, _Y_RI)
    _stamp(Y, 1, 2, _Y_LPAD)
    _stamp_gnd(Y, 2, _Y_CCT)
    _stamp_gnd(Y, 0, _Y_CPAD)

    _stamp(Y, 2, 3, y_Rav)
    _stamp(Y, 3, 4, y_Rdv)
    _stamp(Y, 2, 5, y_Rbv)
    _stamp_gnd(Y, 4, y_Ldv)
    _stamp_gnd(Y, 5, y_Cbv)
    _stamp_gnd(Y, 3, y_Cdv)

    p, q = _PORT_IDX, _INT_IDX
    Y_pp = Y[:, p, :][:, :, p]
    Y_pi = Y[:, p, :][:, :, q]
    Y_ip = Y[:, q, :][:, :, p]
    Y_ii = Y[:, q, :][:, :, q]

    X = np.linalg.solve(Y_ii, Y_ip)
    Y_port = Y_pp - Y_pi @ X

    A = _I2 - Z0 * Y_port
    B = _I2 + Z0 * Y_port
    S = A @ np.linalg.inv(B)

    s11 = S[:, 0, 0]
    s22 = S[:, 1, 1]

    # Flatten to match exactly the 1740D Neural Network Input
    return np.concatenate([
        s11.real, s11.imag,
        s22.real, s22.imag
    ])

