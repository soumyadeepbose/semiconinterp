ax2.set_xlabel('Relative noise level σ/RMS', fontsize=10)
ax2.set_ylabel('Mean MAPE across all parameters (%)', fontsize=10)
ax2.set_title('Operational SNR Floor\n'
              'Red dashed = noise level at which accuracy halves', fontsize=9)
ax2.legend(fontsize=8)
ax2.grid(True, alpha=0.25)
