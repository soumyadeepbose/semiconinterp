ax.legend(fontsize=9)
ax.grid(True, alpha=0.25)
ax.set_xscale('symlog', linthresh=0.001)

ax2 = axes[1]
mean_all_noise = np.array([np.mean([mape_vs_noise[n][i] for n in TARGET_COLS])
                            for i in range(len(NOISE_LEVELS))])
baseline_mape  = mean_all_noise[0]   # σ=0
degradation_db = 20 * np.log10(mean_all_noise / (baseline_mape + 1e-6))

ax2.plot(NOISE_LEVELS, mean_all_noise, 'ko-', lw=2, ms=6, label='Mean MAPE')
