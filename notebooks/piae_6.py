# ── Helpers ───────────────────────────────────────────────────────────────────
def poincare_distance(g1, g2):
    """Poincaré-disk (hyperbolic) metric between two complex Γ values."""
    num   = np.abs(g1 - g2)
    denom = np.abs(1.0 - np.conj(g1) * g2)
    ratio = np.clip(num / (denom + 1e-12), 0.0, 1.0 - 1e-9)
    return 2.0 * np.arctanh(ratio)


def bottleneck_to_X1740(bott_vec):
    """
    Given a 1-D numpy bottleneck vector (scaled, 0-1), return X_1740 via decoder.
    No gradients.
    """
    with torch.no_grad():
        b   = torch.tensor(bott_vec, dtype=torch.float32).unsqueeze(0).to(device)
        z   = model.decoder(b)
        z_u = z * model.score_std + model.score_mean
        Xs  = z_u @ model.V9.T + model.mu_pca
        X1  = Xs * model.feature_std + model.feature_mean
    return X1.cpu().numpy()[0]


def traverse_one_param(param_idx, nominal, n_trav=100):
    """
    Sweep param_idx from 0→1 (scaled), hold others at nominal.
    Returns sweep_vals (n_trav,) and complex S11 array (n_trav, NF).
    """
    sweep_vals = np.linspace(0.0, 1.0, n_trav)
    S11        = np.zeros((n_trav, NF), dtype=complex)
    S22        = np.zeros((n_trav, NF), dtype=complex)

    for i, sv in enumerate(sweep_vals):
        bott              = nominal.copy()
        bott[param_idx]   = sv
        X1740             = bottleneck_to_X1740(bott)
        S11[i] = X1740[0:NF]          + 1j * X1740[NF:2*NF]
        S22[i] = X1740[2*NF:3*NF]    + 1j * X1740[3*NF:]

    return sweep_vals, S11, S22


def draw_smith_background(ax, title=""):
    """Minimal Smith Chart background on given axes."""
    theta = np.linspace(0, 2*np.pi, 400)
    ax.plot(np.cos(theta), np.sin(theta), 'k-', lw=1.2, alpha=0.5)
    ax.axhline(0, color='gray', lw=0.5, alpha=0.3)
    ax.axvline(0, color='gray', lw=0.5, alpha=0.3)
    for r in [0.2, 0.5, 1.0, 2.0]:
        cx, rad = r/(1+r), 1/(1+r)
        ax.add_patch(plt.Circle((cx, 0), rad, fill=False,
                                color='lightgray', lw=0.5))
    # Constant-susceptance arcs (imaginary axis arcs)
    for x in [0.5, 1.0, 2.0]:
        cy, rad = 1/x, 1/x
        for sign in [1, -1]:
            theta_arc = np.linspace(0, np.pi, 200)
            xs = 1 + rad * np.cos(theta_arc)
            ys = sign * cy + rad * np.sin(theta_arc) * sign
            mask = xs**2 + ys**2 <= 1.01
            ax.plot(xs[mask], ys[mask], color='lightgray', lw=0.5)
    ax.set_xlim(-1.15, 1.15)
    ax.set_ylim(-1.15, 1.15)
    ax.set_aspect('equal')
    ax.set_title(title, fontsize=10)
    ax.set_xlabel(r"Re($\Gamma$)", fontsize=8)
    ax.set_ylabel(r"Im($\Gamma$)", fontsize=8)


