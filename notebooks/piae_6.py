# SECTION 4 — INPUT NOISE ROBUSTNESS
#
#   Real VNA measurements are noisy.  This section simulates S-parameter
#   measurement noise directly in the PHYSICAL spectral domain (before PCA),
#   re-encodes through the full PCA pipeline, and measures the degradation
#   in extraction accuracy.
#
#   The noise model is additive Gaussian: S_noisy(f) = S_true(f) + ε(f),
#   where ε ~ N(0, σ²) independently at each frequency and channel.  σ is
#   expressed as a fraction of the RMS signal level (i.e., relative SNR).
#
#   The curve MAPE(σ) tells us: "at what measurement noise level does the
#   model break down?"  This is the operational SNR floor.
#
#   A physics-informed model should be more robust than a pure statistical
#   interpolator because the physics loss biases the encoder towards
#   parameter directions that are orthogonal to noise directions.
# ═══════════════════════════════════════════════════════════════════════════════

print("\n" + "═"*70)
print("SECTION 4 — INPUT NOISE ROBUSTNESS")
print("═"*70)

# Relative noise levels as fraction of per-channel RMS amplitude
NOISE_LEVELS = [0.0, 0.001, 0.005, 0.01, 0.02, 0.05, 0.10, 0.15, 0.20, 0.30]
N_NOISE_TRIALS = 5   # repeated trials per noise level for stable estimates

X_true_t = torch.tensor(X_true_phys_np, dtype=torch.float32).to(device)

# Per-channel RMS signal amplitude (for relative noise scaling)
rms_S11 = float(X_true_t[:, :2*NF].pow(2).mean().sqrt())
rms_S22 = float(X_true_t[:, 2*NF:].pow(2).mean().sqrt())
rms_all  = float(X_true_t.pow(2).mean().sqrt())
print(f"  Signal RMS: S11={rms_S11:.4f}  S22={rms_S22:.4f}  All={rms_all:.4f}")

mape_vs_noise   = {name: [] for name in TARGET_COLS}  # mean over trials
mape_std_noise  = {name: [] for name in TARGET_COLS}  # std over trials

with torch.no_grad():
    for sigma_rel in NOISE_LEVELS:
        sigma_abs = sigma_rel * rms_all   # absolute noise std
        trial_mapes = {name: [] for name in TARGET_COLS}

        for trial in range(N_NOISE_TRIALS):
            noise   = sigma_abs * torch.randn_like(X_true_t)
            X_noisy = X_true_t + noise

            # Re-encode noisy spectrum through PCA pipeline
            x_pca_noisy = model.pca_encode(X_noisy)   # (N, DIM_PCA)
            # Clamp to reasonable range (VNA measurements don't go outside [-2, 2])
            x_pca_noisy = torch.clamp(x_pca_noisy, -5.0, 5.0)

            _, y_bott_noisy, _ = model(x_pca_noisy)
            y_pred_noisy = inv_targets(y_bott_noisy.cpu().numpy())

            for k, name in enumerate(TARGET_COLS):
                mape_k = np.mean(
                    np.abs(y_pred_noisy[:, k] - Y_test_phys[:, k]) /
                    (np.abs(Y_test_phys[:, k]) + 1e-8)
                ) * 100.
                trial_mapes[name].append(mape_k)

        for name in TARGET_COLS:
            mape_vs_noise[name].append(np.mean(trial_mapes[name]))
            mape_std_noise[name].append(np.std(trial_mapes[name]))

        mean_all = np.mean([np.mean(trial_mapes[n]) for n in TARGET_COLS])
        print(f"  σ_rel={sigma_rel:.3f}  mean_MAPE={mean_all:.2f}%")

# Plot ────────────────────────────────────────────────────────────────────────
fig, axes = plt.subplots(1, 2, figsize=(14, 5))

ax = axes[0]
for k, name in enumerate(TARGET_COLS):
    means = np.array(mape_vs_noise[name])
    stds  = np.array(mape_std_noise[name])
    ax.plot(NOISE_LEVELS, means, 'o-', color=colors5[k], lw=1.8, ms=5, label=name)
    ax.fill_between(NOISE_LEVELS, means - stds, means + stds,
                    alpha=0.15, color=colors5[k])
