class Encoder(nn.Module):
    def __init__(self, input_dim=DIM_PCA, output_dim=DIM_PHYS,
                 hidden_dims=[128, 256, 128]):
        super().__init__()
        layers, d = [], input_dim
        for h in hidden_dims:
            layers += [nn.Linear(d, h), nn.LayerNorm(h), nn.ReLU()]
            d = h
        layers += [nn.Linear(d, output_dim), nn.Sigmoid()]
        self.net = nn.Sequential(*layers)
    def forward(self, x): return self.net(x)

class Decoder(nn.Module):
    def __init__(self, input_dim=DIM_PHYS, output_dim=DIM_PCA,
                 hidden_dims=[128, 256, 128]):
        super().__init__()
        layers, d = [], input_dim
        for h in hidden_dims:
            layers += [nn.Linear(d, h), nn.ReLU()]
            d = h
        layers.append(nn.Linear(d, output_dim))
        self.net = nn.Sequential(*layers)
    def forward(self, y): return self.net(y)

class PIAE(nn.Module):
    def __init__(self, V9, mu_pca, score_mean, score_std,
                 feature_mean, feature_std):
        super().__init__()
        self.encoder = Encoder()
        self.decoder = Decoder()
        for name, buf in [('V9', V9), ('mu_pca', mu_pca),
                          ('score_mean', score_mean), ('score_std', score_std),
                          ('feature_mean', feature_mean), ('feature_std', feature_std)]:
            self.register_buffer(name, buf)

    def forward(self, x_pca):
        y_bott  = self.encoder(x_pca)
        z_recon = self.decoder(y_bott)
        X_1740  = self._inv_pca(z_recon)
        return z_recon, y_bott, X_1740

    def decode_to_spectrum(self, y_bott):
        return self._inv_pca(self.decoder(y_bott))

    def _inv_pca(self, z_norm):
        z_unnorm = z_norm * self.score_std + self.score_mean
        X_std    = z_unnorm @ self.V9.T + self.mu_pca
        return X_std * self.feature_std + self.feature_mean

    def pca_encode(self, X_1740_phys):
        """Physical 1740-D spectrum → normalised PCA scores. Useful for noise tests."""
        X_std     = (X_1740_phys - self.feature_mean) / self.feature_std
        X_unnorm  = (X_std - self.mu_pca) @ self.V9        # (B, DIM_PCA)
        return (X_unnorm - self.score_mean) / self.score_std


