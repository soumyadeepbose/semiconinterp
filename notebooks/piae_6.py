# SECTION 1 — ENCODER CROSSTALK MATRIX
#
#   The encoder should extract each physical parameter independently.
#   If changing parameter k_s (source) in the data also shifts the encoder's
#   output for parameter k_e (effect, k_e ≠ k_s), that is *crosstalk* — the
#   model is confusing one parameter's spectral signature with another's.
#
#   Method: for each source parameter k_s, split the test set into
#   LOW (bottom 20%) and HIGH (top 80%) percentile groups.  Measure the
#   mean change in ALL encoder outputs between groups.  Normalise by the
#   self-change (diagonal).  Off-diagonal values > ~0.1 indicate parasitic
#   coupling in the learned extraction.
#
#   Physical interpretation: large crosstalk between (Rb, Rd) or (Cb, Cd)
#   directly reflects the degenerate parameter pairs identified in the PCA
#   analysis — it is the empirical manifestation of the Jacobian near-
#   singularity discussed in the paper's ill-posedness section.
# ═══════════════════════════════════════════════════════════════════════════════

