class Encoder(nn.Module):
    def __init__(self, input_dim=DIM_PCA, output_dim=DIM_PHYS, hidden_dims=[128, 256, 128]):
        super().__init__()
        layers = []
        in_dim = input_dim
        for h in hidden_dims:
            layers += [nn.Linear(in_dim, h), nn.LayerNorm(h), nn.ReLU()]
            in_dim = h
        layers += [nn.Linear(in_dim, output_dim), nn.Sigmoid()]
        self.net = nn.Sequential(*layers)
        self._init_weights()

    def _init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)
    def forward(self, x): return self.net(x)

class Decoder(nn.Module):
    def __init__(self, input_dim: int = DIM_PHYS, output_dim: int = DIM_PCA, hidden_dims=[128, 256, 128]):
        super().__init__()
        layers = []
        in_dim = input_dim
        for h in hidden_dims:
            layers += [nn.Linear(in_dim, h), nn.ReLU()]
            in_dim = h
        layers.append(nn.Linear(in_dim, output_dim))
        self.net = nn.Sequential(*layers)
        self._init_weights()

    def _init_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)
    def forward(self, y_bott: torch.Tensor) -> torch.Tensor: return self.net(y_bott)

class PIAE(nn.Module):
    def __init__(self, V9, mu_pca, score_mean, score_std, feature_mean, feature_std):
        super().__init__()
        self.encoder = Encoder(input_dim=DIM_PCA, output_dim=DIM_PHYS)
        self.decoder = Decoder(input_dim=DIM_PHYS, output_dim=DIM_PCA)

        self.register_buffer('V9',           V9)
        self.register_buffer('mu_pca',       mu_pca)
        self.register_buffer('score_mean',   score_mean)
        self.register_buffer('score_std',    score_std)
        self.register_buffer('feature_mean', feature_mean)
        self.register_buffer('feature_std',  feature_std)

    def forward(self, x_pca: torch.Tensor):
        y_bott  = self.encoder(x_pca)
        z_recon = self.decoder(y_bott)
        z_unnorm = z_recon * self.score_std + self.score_mean
        X_std = z_unnorm @ self.V9.T + self.mu_pca
        X_1740 = X_std * self.feature_std + self.feature_mean
        return z_recon, y_bott, X_1740

def passivity_loss(X_1740: torch.Tensor, Nf: int = 435) -> torch.Tensor:
    R11, I11 = X_1740[..., 0:Nf], X_1740[..., Nf:2*Nf]
    R22, I22 = X_1740[..., 2*Nf:3*Nf], X_1740[..., 3*Nf:]
    penalty_11 = F.relu((R11**2 + I11**2) - 1.0)
    penalty_22 = F.relu((R22**2 + I22**2) - 1.0)
    return torch.mean(penalty_11) + torch.mean(penalty_22)

def smoothness_loss(X_1740: torch.Tensor, Nf: int = 435) -> torch.Tensor:
    R11, I11 = X_1740[..., 0:Nf], X_1740[..., Nf:2*Nf]
    R22, I22 = X_1740[..., 2*Nf:3*Nf], X_1740[..., 3*Nf:]
    def diff_pen(t): return torch.mean((t[..., 1:] - t[..., :-1])**2)
    return diff_pen(R11) + diff_pen(I11) + diff_pen(R22) + diff_pen(I22)

# 🚨 FIX 2: Loss Annealing (Warmup). No more chaotic learnable weights.
def get_annealed_weight(epoch, max_epoch=50):
    """Smooth cosine ramp from 0 to 1 over the first max_epoch epochs"""
    if epoch >= max_epoch:
        return 1.0
    return 0.5 * (1 - math.cos(math.pi * epoch / max_epoch))

def triple_loss(z_recon, x_pca, y_bott, y_gt, X_1740, epoch: int, pw: torch.Tensor, Nf=435):

    L_recon = F.mse_loss(z_recon, x_pca)
    L_phys = torch.mean(pw * (y_bott - y_gt)**2)
    L_passiv = passivity_loss(X_1740, Nf=Nf)
    L_smooth = smoothness_loss(X_1740, Nf=Nf)

    ramp = get_annealed_weight(epoch, max_epoch=100)

    # Static base weights combined with the ramp
    w_recon  = 1.0
    w_phys   = 50.0   # Solid base physical weight
    w_passiv = 0.5 * ramp  # Introduces physics boundaries slowly
    w_smooth = 100.0 * ramp # Scales the gradients effectively

    L_total = (w_recon * L_recon) + (w_phys * L_phys) + (w_passiv * L_passiv) + (w_smooth * L_smooth)

    components = {
        'L_recon'    : L_recon.item(),
        'L_phys'     : L_phys.item(),
        'L_passivity': L_passiv.item(),
        'L_smooth'   : L_smooth.item(),
        'L_total'    : L_total.item(),
    }
    return L_total, components

def train_piae(model, train_loader, val_loader, epochs: int = 300, lr: float = 1e-3):
    pw = torch.tensor(PHYS_WEIGHTS, dtype=torch.float32, device=device)
    optimizer = optim.Adam(model.parameters(), lr=lr, weight_decay=1e-5)

    # Cosine learning rate scheduler
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs, eta_min=1e-5)

    history = {k: [] for k in ['train_total', 'train_recon', 'train_phys', 'train_passivity', 'train_smooth', 'val_total', 'val_mape']}
    best_val_mape = float("inf")
    best_state    = None

    for epoch in range(1, epochs + 1):
        model.train()
        accum  = {k: 0.0 for k in ['total', 'recon', 'phys', 'passivity', 'smooth']}
        n_seen = 0

        for x_b, y_b in train_loader:
            x_b, y_b = x_b.to(device), y_b.to(device)
            optimizer.zero_grad()
            z_recon, y_bott, X_1740 = model(x_b)
            L_total, comp = triple_loss(z_recon, x_b, y_bott, y_b, X_1740, epoch=epoch, pw=pw)
            L_total.backward()
            optimizer.step()

            bs = len(x_b)
            accum['total']     += comp['L_total'] * bs
            accum['recon']     += comp['L_recon'] * bs
            accum['phys']      += comp['L_phys']  * bs
            accum['passivity'] += comp['L_passivity'] * bs
            accum['smooth']    += comp['L_smooth'] * bs
            n_seen             += bs

        for k in accum: accum[k] /= n_seen
        for k in ['total', 'recon', 'phys', 'passivity', 'smooth']: history[f'train_{k}'].append(accum[k])

        model.eval()
        val_total_acc, val_mape_acc, val_seen = 0.0, 0.0, 0
        with torch.no_grad():
            for x_b, y_b in val_loader:
                x_b, y_b = x_b.to(device), y_b.to(device)
                z_recon, y_bott, X_1740 = model(x_b)
                L_total, _ = triple_loss(z_recon, x_b, y_bott, y_b, X_1740, epoch=epoch, pw=pw)
                val_total_acc += L_total.item() * len(x_b)

                y_pred_phys = inverse_transform_targets(y_bott.cpu().numpy())
                y_true_phys = inverse_transform_targets(y_b.cpu().numpy())
                mape = (np.mean(np.abs(y_pred_phys - y_true_phys) / (np.abs(y_true_phys) + 1e-8)) * 100.0)
                val_mape_acc += mape * len(x_b)
                val_seen     += len(x_b)

        val_mape  = val_mape_acc  / val_seen
        history['val_total'].append(val_total_acc / val_seen)
        history['val_mape'].append(val_mape)
        scheduler.step()

        if val_mape < best_val_mape:
            best_val_mape = val_mape
            best_state    = {k: v.cpu().clone() for k, v in model.state_dict().items()}

        if epoch % 20 == 0 or epoch == 1:
            print(f"Epoch {epoch:>3d} | Lrecon={accum['recon']:.5f} | Lphys={accum['phys']:.5f} | LPass={accum['passivity']:.2e} | LSmooth={accum['smooth']:.2e} || val_MAPE={val_mape:.3f}%")

    model.load_state_dict(best_state)
    print(f"  ✅ Best weights restored  (val_MAPE={best_val_mape:.4f}%)")
    return model, history

def evaluate(model, loader, target_cols=TARGET_COLS, split_name='Test'):
    model.eval()
    y_pred_list, y_true_list = [], []
    with torch.no_grad():
        for x_b, y_b in loader:
            _, y_bott, _ = model(x_b.to(device))
            y_pred_list.append(y_bott.cpu().numpy())
            y_true_list.append(y_b.numpy())

    y_pred_phys = inverse_transform_targets(np.concatenate(y_pred_list))
    y_true_phys = inverse_transform_targets(np.concatenate(y_true_list))

    print(f"\n── Per-parameter Metrics [{split_name}] ──────────────────────────")
    mapes, r2s = {}, {}
    for k, name in enumerate(target_cols):
        true, pred = y_true_phys[:, k], y_pred_phys[:, k]
        r2, mape = r2_score(true, pred), np.mean(np.abs(true - pred) / (np.abs(true) + 1e-8)) * 100.
        mapes[name], r2s[name] = mape, r2
        status = "✅ Excellent" if r2 >= 0.95 else "✅ Good" if r2 >= 0.90 else "⚠️ Moderate" if r2 >= 0.70 else "❌ Poor"
        print(f"  {name:>6s}  {r2:>8.4f}  {mape:>8.2f}%  {status}")
    print(f"  {'Mean':>6s}  {np.mean(list(r2s.values())):>8.4f}  {np.mean(list(mapes.values())):>8.2f}%")

    rav_true, rav_pred = y_true_phys[:, target_cols.index('Rav')], y_pred_phys[:, target_cols.index('Rav')]
    fig, axes = plt.subplots(1, 2, figsize=(10, 4))
    axes[0].scatter(rav_true, rav_pred, alpha=0.15, s=4, color='steelblue')
    axes[0].plot([rav_true.min(), rav_true.max()], [rav_true.min(), rav_true.max()], 'r--', lw=1)
    axes[0].set_title(f"Rav parity plot [{split_name}]")
    axes[1].scatter(rav_true, rav_pred - rav_true, alpha=0.15, s=4, color='darkorange')
    axes[1].axhline(0, color='r', linestyle='--', lw=1)
    axes[1].set_title("Rav residual plot (Fixed Scale)")
    plt.savefig(f"rav_residuals_{split_name.lower()}.png", dpi=150)
    plt.show()
    return mapes, r2s

def physical_consistency_report(model, loader):
    model.eval()
    total_passivity, total_smooth, n_seen = 0.0, 0.0, 0
    with torch.no_grad():
        for x_b, _ in loader:
            _, _, X_1740 = model(x_b.to(device))
            total_passivity += passivity_loss(X_1740).item() * len(x_b)
            total_smooth    += smoothness_loss(X_1740).item() * len(x_b)
            n_seen          += len(x_b)
    print(f"\nPhysical Consistency (test set):\n  Passivity Violation Floor: {total_passivity/n_seen:.6e}\n  Smoothness Loss: {total_smooth/n_seen:.6e}")
    return total_passivity/n_seen, total_smooth/n_seen

