def raw_to_encoder_output(x_raw_batch: torch.Tensor) -> torch.Tensor:
    """
    Differentiable pipeline: X_raw → encoder output (5-dim, Sigmoid-bounded).
    All transforms linear except the MLP itself.
    x_raw_batch shape: (B, 1740), requires_grad must be set by caller.
    """
    x_std     = (x_raw_batch - _feat_mean_cpu) / _feat_std_cpu     # standardise
    x_pca_un  = (x_std - _mu_pca_cpu) @ _V9_cpu                    # PCA project (B, N)
    x_pca_n   = (x_pca_un - _score_mean_cpu) / _score_std_cpu      # score-normalise
    # Run encoder on CPU (avoids device mismatch in gradient tape)
    enc_cpu   = model.encoder.cpu()
    return enc_cpu(x_pca_n)


def integrated_gradients_raw(x_raw_sample: np.ndarray,
                              target_param_idx: int,
                              n_steps: int = 50) -> np.ndarray:
    """
    Compute IG for one sample w.r.t. the full 1740-dim raw input.
    Baseline = feature_mean  (= zero in standardised space).

    Returns ig_raw: shape (1740,)
    """
    baseline = _feat_mean_cpu.numpy()                   # zero baseline in std space
    x        = x_raw_sample.astype(np.float32)

    alphas   = np.linspace(0.0, 1.0, n_steps)
    grads    = []

    for alpha in alphas:
        interp_np = baseline + alpha * (x - baseline)
        interp_t  = torch.tensor(interp_np, dtype=torch.float32).unsqueeze(0)
        interp_t.requires_grad_(True)

        out = raw_to_encoder_output(interp_t)           # (1, 5)
        out[0, target_param_idx].backward()

        grads.append(interp_t.grad.detach().squeeze(0).numpy())

    avg_grad = np.stack(grads).mean(axis=0)             # (1740,)
    ig_raw   = (x - baseline) * avg_grad
    return ig_raw


def ig_to_freq_attribution(ig_raw: np.ndarray) -> np.ndarray:
    """
    Collapse the 1740-dim IG vector into a per-frequency attribution curve.
    Channels: [S11_Re(0:NF), S11_Im(NF:2NF), S22_Re(2NF:3NF), S22_Im(3NF:)]
    Returns shape (NF,) — average absolute IG across all 4 channels.
    """
    s11_re = np.abs(ig_raw[0:NF])
    s11_im = np.abs(ig_raw[NF:2*NF])
    s22_re = np.abs(ig_raw[2*NF:3*NF])
    s22_im = np.abs(ig_raw[3*NF:])
    return (s11_re + s11_im + s22_re + s22_im) / 4.0


