def _inverse(Y_sc, target_cols, scalers):
    Y_out = np.zeros_like(Y_sc, dtype=np.float64)
    for i, t in enumerate(target_cols):
        Y_out[:, i] = scalers[t].inverse_transform(Y_sc[:, i:i+1]).ravel()
        Y_out[:, i] = 10.0 ** Y_out[:, i]
    return Y_out

