def run_piae_inference(model, loader):
    """Returns predictions and ground truth for all 5 PIAE observable params."""
    pred_list, true_list = [], []
    with torch.no_grad():
        for x_b, y_b in loader:
            _, y_bott, _, _ = model(x_b.to(device))
            pred_list.append(y_bott.cpu().numpy())
            # y_b has 6 cols (CVAE ordering) — remap to 5-param PIAE ordering
            true_list.append(y_b.cpu().numpy())

    # y_b is in CVAE 6-param scaled space; extract & invert only obs params
    pred_scaled  = np.concatenate(pred_list)           # (N, 5) already PIAE output
