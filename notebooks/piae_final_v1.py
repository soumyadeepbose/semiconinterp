def evaluate_cvae(model, loader, split_name='Test'):
    model.eval()
    mu_list, y_list = [], []
    with torch.no_grad():
        for x_b, y_b in loader:
            _, mu, _, _, _ = model(x_b.to(device))
            mu_list.append(mu.cpu().numpy())
            y_list.append(y_b.numpy())

    y_pred = inverse_transform_targets(np.concatenate(mu_list))
    y_true = inverse_transform_targets(np.concatenate(y_list))

    print(f"\n── Per-parameter Metrics [{split_name}] ──────────────────────────")
    smapes, r2s = {}, {}
    for k, name in enumerate(TARGET_COLS):
        t, p  = y_true[:, k], y_pred[:, k]
        r2    = r2_score(t, p)
        smape = np.mean(200.0 * np.abs(t - p) / (np.abs(t) + np.abs(p) + 1e-8))
        smapes[name], r2s[name] = smape, r2
        flag  = " [null-space — poor R² expected]" if name == 'Ldv' else ""
        qual  = ("✅ Excellent" if r2 >= 0.95 else "✅ Good" if r2 >= 0.90
                 else "⚠️ Moderate" if r2 >= 0.70 else "❌ Poor")
        print(f"  {name:>6s}  R²={r2:>8.4f}  SMAPE={smape:>7.3f}%  {qual}{flag}")

    obs_r2    = np.mean([r2s[TARGET_COLS[i]]    for i in OBS_IDX])
    obs_smape = np.mean([smapes[TARGET_COLS[i]] for i in OBS_IDX])
    print(f"\n  Mean (5 observable):  R²={obs_r2:.4f}  SMAPE={obs_smape:.3f}%")
    print(f"  Mean (all 6):         R²={np.mean(list(r2s.values())):.4f}  "
          f"SMAPE={np.mean(list(smapes.values())):.3f}%")
    return smapes, r2s


def ld_unobservability_report(model, loader):
    """
    The definitive null-space diagnostic for Architecture Idea 2.
    Expected outcome after stop-gradient fix:
      σ_Ld ≈ 1.0 (KL equilibrium, no reconstruction pressure)
      σ_obs < 1.0 (reconstruction pressure pushes these down)
      Ratio >> 1.5
    """
    model.eval()
    sigma_all = []
    with torch.no_grad():
        for x_b, _ in loader:
            _, _, logvar, _, _ = model(x_b.to(device))
            sigma_all.append(torch.exp(0.5 * logvar).cpu().numpy())
    sigma_all  = np.concatenate(sigma_all)
    mean_sigma = sigma_all.mean(axis=0)

    mean_sigma_obs = mean_sigma[OBS_IDX].mean()
    ratio          = mean_sigma[LDV_IDX] / (mean_sigma_obs + 1e-8)

    print("\n── Posterior σ per Parameter (Ld Unobservability Diagnostic) ─────")
    print(f"  {'Param':>6s}  {'Mean σ':>10s}  {'Min σ':>10s}  {'Max σ':>10s}  {'Governed by':>15s}")
    for i, name in enumerate(TARGET_COLS):
        gov    = "KL + stop-grad" if i == LDV_IDX else "KL + Lrecon"
        marker = " ◄" if i == LDV_IDX else ""
        print(f"  {name:>6s}  {mean_sigma[i]:>10.4f}  "
              f"{sigma_all[:,i].min():>10.4f}  "
              f"{sigma_all[:,i].max():>10.4f}  "
              f"{gov:>15s}{marker}")

    print(f"\n  σ_Ldv         = {mean_sigma[LDV_IDX]:.4f}")
    print(f"  σ̄_observable  = {mean_sigma_obs:.4f}")
    print(f"  Ratio         = {ratio:.2f}×")

    if ratio > 2.5:
        print("  ✅ NULL SPACE STRONGLY CONFIRMED — posterior correctly uninformed about Ld.")
    elif ratio > 1.5:
        print("  ✅ NULL SPACE CONFIRMED.")
    else:
        print("  ⚠️  Ratio < 1.5 — stop-gradient may not have taken effect or β too low.")
    return sigma_all


