def _build_scalers(target_cols):
    """Reconstruct y_scalers for a given target list."""
    Y_raw  = df_pca[target_cols].values.astype(np.float64)
    Y_proc = Y_raw.copy()
    for i in range(len(target_cols)):
        Y_proc[:, i] = np.log10(np.abs(Y_proc[:, i]) + 1e-30)
    scalers  = {}
    Y_scaled = np.zeros_like(Y_proc, dtype=np.float32)
    for i, t in enumerate(target_cols):
        sc = StandardScaler()
        sc.fit(Y_proc[train_idx_np, i:i+1])
        Y_scaled[:, i] = sc.transform(Y_proc[:, i:i+1]).ravel()
        scalers[t] = sc
    return scalers, Y_scaled

