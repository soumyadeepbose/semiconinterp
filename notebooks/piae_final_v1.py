def evaluate(model, loader, split_name='Test'):
    model.eval()
    y_pred_list, y_true_list = [], []
    with torch.no_grad():
        for x_b, y_b in loader:
            _, y_bott, _, _ = model(x_b.to(device))
            y_pred_list.append(y_bott.cpu().numpy())
            y_true_list.append(y_b.numpy())

    y_pred_phys = inverse_transform_targets(np.concatenate(y_pred_list))
    y_true_phys = inverse_transform_targets(np.concatenate(y_true_list))

    print(f"\n── Per-parameter Metrics [{split_name}] ──────────────────────────")
    mapes, r2s = {}, {}
    for k, name in enumerate(TARGET_COLS):
        t, p = y_true_phys[:, k], y_pred_phys[:, k]
        r2   = r2_score(t, p)
        mape = np.mean(np.abs(t - p) / (np.abs(t) + 1e-8)) * 100.0
        mapes[name], r2s[name] = mape, r2
        status = ("✅ Excellent" if r2 >= 0.95 else "✅ Good" if r2 >= 0.90
                  else "⚠️ Moderate" if r2 >= 0.70 else "❌ Poor")
        print(f"  {name:>6s}  R²={r2:>8.4f}  MAPE={mape:>7.3f}%  {status}")
    print(f"  {'Mean':>6s}  R²={np.mean(list(r2s.values())):>8.4f}  "
          f"MAPE={np.mean(list(mapes.values())):>7.3f}%")
    return mapes, r2s

