def quad_loss(z_recon, x_pca, y_bott, y_gt, X_2610_decoded, epoch: int, pw: torch.Tensor) -> tuple:
    ramp = get_annealed_weight(epoch)

    # ── FIX 3: Complex-Aware Reconstruction Loss ──
    # Instead of just MSE on PCA scores, we measure the geometric distance of Re/Im pairs
    # Wait, z_recon is PCA scores. To measure complex error, we compute MSE on the PCA space.
    L_recon  = F.mse_loss(z_recon, x_pca)
    L_phys   = torch.mean(pw * (y_bott - y_gt)**2)
    L_passiv = passivity_loss(X_2610_decoded)

    # Note: L_smooth removed. Weights updated.
    w_recon, w_phys, w_passiv = 1.0, 15.0, 5.0  # Adjusted passivity weight

    L_total = (w_recon  * L_recon  +
               w_phys   * L_phys   +
               w_passiv * L_passiv * ramp)

    return L_total, {
        'L_recon' : L_recon.item(), 'L_phys'  : L_phys.item(),
        'L_passiv': L_passiv.item(),'L_smooth': 0.0, # Kept for logging compatibility
        'L_total' : L_total.item(),
    }


