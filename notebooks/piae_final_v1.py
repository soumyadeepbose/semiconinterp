    np.random.seed(seed)

    pw        = torch.tensor(PHYS_WEIGHTS, dtype=torch.float32, device=device)

    # ── FIX: Increased weight decay from 1e-5 to 1e-4 for better generalization ──
    optimizer = optim.Adam(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=epochs, eta_min=1e-5)

    loss_keys = ['total','recon','phys','passiv','smooth']
    history   = {f'train_{k}': [] for k in loss_keys}
    history.update({'val_total': [], 'val_mape': []})

    best_val_mape = float('inf')
    best_state    = None
    best_epoch    = -1

    for epoch in range(1, epochs + 1):
        model.train()
        accum  = {k: 0.0 for k in loss_keys}
        n_seen = 0

        for x_b, y_b in train_loader:
            x_b, y_b = x_b.to(device), y_b.to(device)
            optimizer.zero_grad()

            z_recon, y_bott, X_2610_dec, _ = model(x_b)

            L_total, comp = quad_loss(
                z_recon, x_b, y_bott, y_b,
                X_2610_dec, epoch, pw
            )
            L_total.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()

            bs = len(x_b)
            for k in loss_keys: accum[k] += comp[f'L_{k}'] * bs
            n_seen += bs

        for k in loss_keys:
            accum[k] /= n_seen
            history[f'train_{k}'].append(accum[k])

        # ── Validation ────────────────────────────────────────────────────
        model.eval()
        val_total_acc = val_mape_acc = val_seen = 0
        with torch.no_grad():
            for x_b, y_b in val_loader:
                x_b, y_b = x_b.to(device), y_b.to(device)
                z_recon, y_bott, X_2610_dec, _ = model(x_b)

                L_total, _ = quad_loss(
                    z_recon, x_b, y_bott, y_b,
                    X_2610_dec, epoch, pw
                )
                val_total_acc += L_total.item() * len(x_b)

                y_pred_p = inverse_transform_targets(y_bott.cpu().numpy())
                y_true_p = inverse_transform_targets(y_b.cpu().numpy())
                mape     = np.mean(np.abs(y_pred_p - y_true_p) /
                                   (np.abs(y_true_p) + 1e-8)) * 100.0
                val_mape_acc += mape * len(x_b)
                val_seen     += len(x_b)

        val_mape = val_mape_acc / val_seen
        history['val_total'].append(val_total_acc / val_seen)
        history['val_mape'].append(val_mape)
        scheduler.step()

        if val_mape < best_val_mape:
            best_val_mape = val_mape
            best_state    = {k: v.cpu().clone() for k, v in model.state_dict().items()}
            best_epoch    = epoch

        if epoch % 20 == 0 or epoch == 1:
            print(f"Ep {epoch:>3d} | "
                  f"Lrec={accum['recon']:.5f} | Lphy={accum['phys']:.5f} | "
                  f"LPas={accum['passiv']:.2e} | "
                  f"LSmo={accum['smooth']:.2e} || val_MAPE={val_mape:.3f}%")

    model.load_state_dict(best_state)
    print(f"\n ✅ Best weights restored  (val_MAPE={best_val_mape:.4f}%  @ epoch {best_epoch}/{epochs})")
    return model, history, best_epoch


