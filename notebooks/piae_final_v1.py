DIM_PHYS_PIAE = 5   # Ld excluded

class PIAEEncoder(nn.Module):
    def __init__(self, input_dim=DIM_PCA, output_dim=DIM_PHYS_PIAE,
                 hidden_dims=(128, 256, 128)):
        super().__init__()
        layers, d = [], input_dim
        for h in hidden_dims:
            layers += [nn.Linear(d, h), nn.LayerNorm(h), nn.ReLU()]
            d = h
        layers.append(nn.Linear(d, output_dim))
        self.net = nn.Sequential(*layers)
    def forward(self, x): return self.net(x)

class PIAEDecoder(nn.Module):
    def __init__(self, input_dim=DIM_PHYS_PIAE, output_dim=DIM_PCA,
                 hidden_dims=(128, 256, 128)):
        super().__init__()
        layers, d = [], input_dim
        for h in hidden_dims:
            layers += [nn.Linear(d, h), nn.ReLU()]
            d = h
        layers.append(nn.Linear(d, output_dim))
        self.net = nn.Sequential(*layers)
    def forward(self, y): return self.net(y)

class PIAE(nn.Module):
    def __init__(self, V_pca, mu_pca, score_mean, score_std, feature_mean, feature_std):
        super().__init__()
        self.encoder     = PIAEEncoder()
        self.decoder     = PIAEDecoder()
        self.residual_net = nn.Sequential(
            nn.Linear(DIM_RAW, 512), nn.ReLU(), nn.Linear(512, DIM_RAW))
        nn.init.zeros_(self.residual_net[-1].weight)
        nn.init.zeros_(self.residual_net[-1].bias)
        self.register_buffer('V_pca',        V_pca)
        self.register_buffer('mu_pca',       mu_pca)
        self.register_buffer('score_mean',   score_mean)
        self.register_buffer('score_std',    score_std)
        self.register_buffer('feature_mean', feature_mean)
        self.register_buffer('feature_std',  feature_std)

    def pca_invert(self, z_norm):
        z_unnorm = z_norm * self.score_std + self.score_mean
        X_std    = z_unnorm @ self.V_pca.T + self.mu_pca
        return X_std * self.feature_std + self.feature_mean

    def forward(self, x_pca):
        y_bott          = self.encoder(x_pca)
        z_recon         = self.decoder(y_bott)
        X_pca_only      = self.pca_invert(z_recon)
        X_2610_true     = self.pca_invert(x_pca)
        X_2610_decoded  = X_pca_only + 0.01 * self.residual_net(X_pca_only)
        return z_recon, y_bott, X_2610_decoded, X_2610_true


