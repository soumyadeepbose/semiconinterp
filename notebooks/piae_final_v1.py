class Encoder(nn.Module):
    def __init__(self, input_dim=DIM_PCA, output_dim=DIM_PHYS, hidden_dims=(128, 256, 128)):
        super().__init__()
        layers, in_d = [], input_dim
        for h in hidden_dims:
            # ── FIX: Added Dropout for regularization ──
            layers += [nn.Linear(in_d, h), nn.LayerNorm(h), nn.ReLU()]
            in_d = h

        # ── FIX: Removed nn.Sigmoid() so output matches StandardScaler bounds (-inf to +inf) ──
        layers += [nn.Linear(in_d, output_dim)]
        self.net = nn.Sequential(*layers)
        self._init()

    def _init(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)

    def forward(self, x): return self.net(x)


class Decoder(nn.Module):
    def __init__(self, input_dim=DIM_PHYS, output_dim=DIM_PCA, hidden_dims=(128, 256, 128)):
        super().__init__()
        layers, in_d = [], input_dim
        for h in hidden_dims:
            # ── FIX: Added Dropout for regularization ──
            layers += [nn.Linear(in_d, h), nn.ReLU()]
            in_d = h
        layers.append(nn.Linear(in_d, output_dim))
        self.net = nn.Sequential(*layers)
        self._init()

    def _init(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)

    def forward(self, y_bott): return self.net(y_bott)


