# ║                                                                           ║
# ║  Evaluation philosophy:                                                   ║
# ║  • Both models evaluated on the IDENTICAL held-out test set               ║
# ║  • IDENTICAL metric (MAPE) on IDENTICAL parameter set (5 observable)      ║
# ║  • Ldv is reported separately as a UQ diagnostic for CVAE only            ║
# ║  • SMAPE is NOT used — it is numerically incompatible with MAPE           ║
# ║                                                                           ║
# ║  Why MAPE not SMAPE:                                                      ║
# ║    MAPE  = |y_true - y_pred| / |y_true|                                  ║
# ║    SMAPE = 2|y_true - y_pred| / (|y_true| + |y_pred|)                    ║
# ║    These produce DIFFERENT numbers on identical predictions.               ║
# ║    A PIAE MAPE of 2.6% vs CVAE SMAPE of 1.8% is an invalid comparison.  ║
# ║    Since PIAE used MAPE, CVAE must also use MAPE for a fair table.        ║
# ║    Ldv MAPE is reported but excluded from model selection and means.      ║
