# Physics-constraint loss terms (UPDATED FOR RIGOR)
# ─────────────────────────────────────────────────────────────────────────────
def passivity_loss(X_2610: torch.Tensor, Nf: int = NF) -> torch.Tensor:
    # Extract components
    R11, I11 = X_2610[..., :Nf],       X_2610[..., Nf:2*Nf]
    R21, I21 = X_2610[..., 2*Nf:3*Nf], X_2610[..., 3*Nf:4*Nf]
    R22, I22 = X_2610[..., 4*Nf:5*Nf], X_2610[..., 5*Nf:]

    # Magnitudes squared
    mag_S11_sq = R11**2 + I11**2
    mag_S22_sq = R22**2 + I22**2
    mag_S21_sq = R21**2 + I21**2  # Assuming S12 = S21

    # Condition 1 & 2: Individual port passivity
    loss_port1 = torch.mean(F.relu(mag_S11_sq + mag_S21_sq - 1.0))
    loss_port2 = torch.mean(F.relu(mag_S22_sq + mag_S21_sq - 1.0))

    # Condition 3: Strict Unconditional Passivity (Determinant condition)
    # Det(S) = S11*S22 - S21^2
    det_Re = R11*R22 - I11*I22 - (R21**2 - I21**2)
    det_Im = R11*I22 + I11*R22 - (2*R21*I21)
    mag_det_sq = det_Re**2 + det_Im**2

    # 1 - |S11|^2 - |S22|^2 - 2|S21|^2 + |Det|^2 >= 0
    det_violation = mag_S11_sq + mag_S22_sq + 2*mag_S21_sq - mag_det_sq - 1.0
    loss_det = torch.mean(F.relu(det_violation))

    return loss_port1 + loss_port2 + loss_det

# (SMOOTHNESS LOSS IS ENTIRELY DELETED AS IT IS REDUNDANT WITH PCA)

def get_annealed_weight(epoch: int, max_epoch: int = 200) -> float:
    if epoch >= max_epoch: return 1.0
    return 0.5 * (1.0 - math.cos(math.pi * epoch / max_epoch))


