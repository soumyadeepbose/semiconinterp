class CVAEEncoder(nn.Module):
    def __init__(self, input_dim=DIM_PCA, latent_dim=DIM_PHYS,
                 hidden_dims=(128, 256, 256, 128)):
        super().__init__()
        layers, d = [], input_dim
        for h in hidden_dims:
            layers += [nn.Linear(d, h), nn.LayerNorm(h), nn.GELU()]
            d = h
        self.shared    = nn.Sequential(*layers)
        self.fc_mu     = nn.Linear(d, latent_dim)
        self.fc_logvar = nn.Linear(d, latent_dim)
        self._init()

    def _init(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)
        nn.init.zeros_(self.fc_logvar.weight)
        nn.init.zeros_(self.fc_logvar.bias)   # σ ≈ 1 at init

    def forward(self, x):
        h      = self.shared(x)
        mu     = self.fc_mu(h)
        logvar = torch.clamp(self.fc_logvar(h), min=-10.0, max=4.0)
        return mu, logvar


class CVAEDecoder(nn.Module):
    def __init__(self, latent_dim=DIM_PHYS, output_dim=DIM_PCA,
                 hidden_dims=(128, 256, 256, 128)):
        super().__init__()
        layers, d = [], latent_dim
        for h in hidden_dims:
            layers += [nn.Linear(d, h), nn.GELU()]
            d = h
        layers.append(nn.Linear(d, output_dim))
        self.net = nn.Sequential(*layers)
        self._init()

    def _init(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_uniform_(m.weight)
                nn.init.zeros_(m.bias)

    def forward(self, z):
        return self.net(z)


class PhysicsSupervisedCVAE(nn.Module):
    """
    Physics-Supervised Conditional VAE — Architecture Idea 2 (v5).

    Key architectural change: stop-gradient on z_Ld entering the decoder.

    Without stop-gradient (v1–v4 bug):
        L_recon depends on z_Ld → ∂L_recon/∂σ_Ld < 0 → reconstruction
        pressure pushes σ_Ld down, cancelling the KL's tendency to raise it.

    With stop-gradient (v5 fix):
        ∂L_recon/∂σ_Ld = 0 exactly. Only the KL term governs σ_Ld.
        KL equilibrium: σ_Ld → 1.0.
        Observable params still have reconstruction pressure → σ_obs < 1.0.
        Resulting ratio: σ_Ld / σ̄_obs > 1.5 (expected ~2-4×).
    """
    def __init__(self, V_pca, mu_pca, score_mean, score_std,
                 feature_mean, feature_std):
        super().__init__()
        self.encoder = CVAEEncoder()
        self.decoder = CVAEDecoder()
        self.register_buffer('V_pca',        V_pca)
        self.register_buffer('mu_pca',       mu_pca)
        self.register_buffer('score_mean',   score_mean)
        self.register_buffer('score_std',    score_std)
        self.register_buffer('feature_mean', feature_mean)
        self.register_buffer('feature_std',  feature_std)

    def pca_invert(self, z_norm):
        z_unnorm = z_norm * self.score_std + self.score_mean
        X_std    = z_unnorm @ self.V_pca.T + self.mu_pca
        return X_std * self.feature_std + self.feature_mean

    def reparameterise(self, mu, logvar):
        return mu + torch.exp(0.5 * logvar) * torch.randn_like(mu)

    def forward(self, x_pca):
        mu, logvar = self.encoder(x_pca)
        z          = self.reparameterise(mu, logvar)

        # ── FIX [1]: Stop gradient through z_Ld in the decoder path ────────
        # This severs ∂L_recon/∂σ_Ld completely. The decoder still receives
        # a value for the Ldv slot (so architecture is unchanged), but that
        # value is treated as a constant from the optimiser's perspective.
        # Only KL (∂L_KL/∂σ_Ld) remains → σ_Ld converges to its KL optimum.
        z_for_decoder = torch.cat([
            z[:, :LDV_IDX],
            z[:, LDV_IDX:LDV_IDX + 1].detach(),   # ← stop-gradient here
            z[:, LDV_IDX + 1:]
        ], dim=1)

        z_recon        = self.decoder(z_for_decoder)
        X_2610_decoded = self.pca_invert(z_recon)
        X_2610_true    = self.pca_invert(x_pca)
        return z_recon, mu, logvar, X_2610_decoded, X_2610_true

    @torch.no_grad()
    def sample_posterior(self, x_pca, n_samples=500):
        mu, logvar = self.encoder(x_pca)
        sigma      = torch.exp(0.5 * logvar)
        eps        = torch.randn(n_samples, x_pca.shape[0], DIM_PHYS, device=x_pca.device)
        return mu.unsqueeze(0) + sigma.unsqueeze(0) * eps, mu, sigma


# ─────────────────────────────────────────────────────────────────────────────
# § 4  Physics Loss: Strict Passivity (unchanged from v4)
# ─────────────────────────────────────────────────────────────────────────────

def passivity_loss(X_2610, Nf=NF):
    R11 = X_2610[..., :Nf];           I11 = X_2610[..., Nf:2*Nf]
    R21 = X_2610[..., 2*Nf:3*Nf];    I21 = X_2610[..., 3*Nf:4*Nf]
    R22 = X_2610[..., 4*Nf:5*Nf];    I22 = X_2610[..., 5*Nf:]

    mag_S11_sq = R11**2 + I11**2
    mag_S22_sq = R22**2 + I22**2
    mag_S21_sq = R21**2 + I21**2

    loss_port1 = torch.mean(F.relu(mag_S11_sq + mag_S21_sq - 1.0))
    loss_port2 = torch.mean(F.relu(mag_S22_sq + mag_S21_sq - 1.0))

    det_Re = R11*R22 - I11*I22 - (R21**2 - I21**2)
    det_Im = R11*I22 + I11*R22 - 2.0*R21*I21
    mag_det_sq    = det_Re**2 + det_Im**2
    det_violation = mag_S11_sq + mag_S22_sq + 2.0*mag_S21_sq - mag_det_sq - 1.0
    return loss_port1 + loss_port2 + torch.mean(F.relu(det_violation))


def get_annealed_weight(epoch, max_epoch=200):
    if epoch >= max_epoch: return 1.0
    return 0.5 * (1.0 - math.cos(math.pi * epoch / max_epoch))


# ─────────────────────────────────────────────────────────────────────────────
# § 5  CVAE Loss Function (v5)
# ─────────────────────────────────────────────────────────────────────────────

def cvae_loss(z_recon, x_pca, mu, logvar, y_gt,
              X_2610_decoded, epoch, pw,
              beta=BETA_KL, beta_ldv_mult=BETA_LDV_MULT,
              lambda1=LAMBDA1, lambda_p=LAMBDA_PASSIV,
              free_bits=FREE_BITS):
    """
    L = L_recon  +  KL_weighted  +  λ₁·L_phys  +  λ_p·L_passiv·ramp

    KL_weighted uses per-dimension β weights:
      • Observable dims : β_k = BETA_KL  (e.g. 0.3)
      • Ldv dim         : β_Ld = BETA_KL × BETA_LDV_MULT  (e.g. 3.0)

    Free bits on observable dims: KL_k = max(KL_k, FREE_BITS)
      Prevents complete σ collapse for easy-to-predict params (e.g. Cbv).
      Ldv does NOT get free-bits clamping — we want its KL to reflect the
      actual posterior, not an artificial floor.

    With stop-gradient in forward():
      ∂L_recon/∂σ_Ld = 0 → KL alone governs σ_Ld → σ_Ld → 1.0
      Observable σ values stay <1.0 from reconstruction pressure.
    """
    ramp = get_annealed_weight(epoch)

    # 1. PCA-space reconstruction
    L_recon = F.mse_loss(z_recon, x_pca)

    # 2. Per-dim KL w.r.t. physics-conditional prior p(z|Ygt) = 𝒩(Yscaled_gt, I)
    sigma2      = torch.exp(logvar)
    kl_per_dim  = 0.5 * (sigma2 + (mu - y_gt)**2 - 1.0 - logvar)  # (B, DIM_PHYS)

    # Build per-dim beta weights
    # beta_weights = torch.ones(DIM_PHYS, dtype=torch.float32, device=mu.device) * beta
    # beta_weights[LDV_IDX] = beta * beta_ldv_mult

    # # Apply free-bits floor to observable dims only
    # kl_weighted = kl_per_dim * beta_weights                         # (B, DIM_PHYS)
    # for obs_i in OBS_IDX:
    #     kl_weighted[:, obs_i] = torch.clamp(kl_weighted[:, obs_i],
    #                                         min=free_bits * beta_weights[obs_i])
    # L_kl = kl_weighted.mean()

    # Build per-dim beta weights
    beta_weights = torch.ones(DIM_PHYS, dtype=torch.float32, device=mu.device) * beta
    beta_weights[LDV_IDX] = beta * beta_ldv_mult

    # Create free-bits thresholds: FREE_BITS for observables, 0.0 for Ldv
    thresholds = torch.zeros(DIM_PHYS, device=mu.device)
    thresholds[OBS_IDX] = free_bits

    # Apply free-bits clamping safely (out-of-place)
    kl_clamped = torch.max(kl_per_dim, thresholds.unsqueeze(0))

    # Apply betas
    kl_weighted = kl_clamped * beta_weights.unsqueeze(0)
    L_kl = kl_weighted.mean()

    # 3. Explicit phys supervision on μ (Ldv weight = 0 → only KL governs it)
    L_phys = torch.mean(pw * (mu - y_gt)**2)

    # 4. Passivity (annealed; λ=5 since data is already passive)
    L_passiv = passivity_loss(X_2610_decoded)

    L_total = L_recon + L_kl + lambda1 * L_phys + lambda_p * L_passiv * ramp

    return L_total, {
        'L_total' : L_total.item(),
        'L_recon' : L_recon.item(),
        'L_kl'    : L_kl.item(),
        'L_phys'  : L_phys.item(),
        'L_passiv': L_passiv.item(),
    }


# ─────────────────────────────────────────────────────────────────────────────
# § 6  Training Loop
# ─────────────────────────────────────────────────────────────────────────────

def _smape_obs(y_pred_phys, y_true_phys):
    """SMAPE on the 5 observable params only (excludes Ldv)."""
    p = y_pred_phys[:, OBS_IDX]
    t = y_true_phys[:, OBS_IDX]
    return np.mean(200.0 * np.abs(p - t) / (np.abs(t) + np.abs(p) + 1e-8))


def train_cvae(model, train_loader, val_loader,
               epochs=500, lr=1e-3, seed=42):
    torch.manual_seed(seed); np.random.seed(seed)

    pw        = torch.tensor(PHYS_WEIGHTS, dtype=torch.float32, device=device)
    optimizer = optim.Adam(model.parameters(), lr=lr, weight_decay=1e-4)
    scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(
        optimizer, T_max=epochs, eta_min=1e-5)

    loss_keys = ['total', 'recon', 'kl', 'phys', 'passiv']
    history   = {f'train_{k}': [] for k in loss_keys}
    history.update({'val_total'     : [],
                    'val_smape_obs' : [],   # 5-param SMAPE (used for checkpointing)
                    'val_smape_all' : [],   # 6-param SMAPE (diagnostic only)
                    'sigma_ld'      : [],
                    'sigma_obs_mean': []})

    best_val_smape = float('inf')
    best_state     = None
    best_epoch     = -1

    for epoch in range(1, epochs + 1):
        # ── Train ─────────────────────────────────────────────────────────
        model.train()
        accum  = {k: 0.0 for k in loss_keys}
        n_seen = 0

        for x_b, y_b in train_loader:
            x_b, y_b = x_b.to(device), y_b.to(device)
            optimizer.zero_grad()
            z_recon, mu, logvar, X_dec, _ = model(x_b)
            L_total, comp = cvae_loss(z_recon, x_b, mu, logvar, y_b, X_dec, epoch, pw)
            L_total.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
            optimizer.step()
            bs = len(x_b)
            for k in loss_keys: accum[k] += comp[f'L_{k}'] * bs
            n_seen += bs

        for k in loss_keys:
            accum[k] /= n_seen
            history[f'train_{k}'].append(accum[k])

        # ── Validation ────────────────────────────────────────────────────
        model.eval()
        val_total_acc  = 0.0
        smape_obs_acc  = smape_all_acc  = 0.0
        sigma_ld_acc   = sigma_obs_acc  = 0.0
        val_seen       = 0

        with torch.no_grad():
            for x_b, y_b in val_loader:
                x_b, y_b = x_b.to(device), y_b.to(device)
                z_recon, mu, logvar, X_dec, _ = model(x_b)

                L_total, _ = cvae_loss(z_recon, x_b, mu, logvar, y_b, X_dec, epoch, pw)
                val_total_acc += L_total.item() * len(x_b)

                sigma = torch.exp(0.5 * logvar)
                sigma_ld_acc  += sigma[:, LDV_IDX].mean().item() * len(x_b)
                sigma_obs_acc += sigma[:, OBS_IDX].mean().item() * len(x_b)

                y_pred_p = inverse_transform_targets(mu.cpu().numpy())
                y_true_p = inverse_transform_targets(y_b.cpu().numpy())

                smape_obs_acc += _smape_obs(y_pred_p, y_true_p) * len(x_b)
                smape_all_acc += np.mean(200.0 * np.abs(y_pred_p - y_true_p) /
                                         (np.abs(y_true_p) + np.abs(y_pred_p) + 1e-8)) * len(x_b)
                val_seen += len(x_b)

        val_smape_obs  = smape_obs_acc  / val_seen
        val_smape_all  = smape_all_acc  / val_seen
        s_ld           = sigma_ld_acc   / val_seen
        s_obs          = sigma_obs_acc  / val_seen

        history['val_total'].append(val_total_acc  / val_seen)
        history['val_smape_obs'].append(val_smape_obs)
        history['val_smape_all'].append(val_smape_all)
        history['sigma_ld'].append(s_ld)
        history['sigma_obs_mean'].append(s_obs)
        scheduler.step()

        # ── Checkpoint on 5-param SMAPE (Ldv excluded) ────────────────────
        if val_smape_obs < best_val_smape:
            best_val_smape = val_smape_obs
            best_state     = {k: v.cpu().clone() for k, v in model.state_dict().items()}
            best_epoch     = epoch

        if epoch % 20 == 0 or epoch == 1:
            ratio = s_ld / (s_obs + 1e-8)
            print(f"Ep {epoch:>3d} | "
                  f"Lrec={accum['recon']:.5f} | "
                  f"LKL={accum['kl']:.5f} | "
                  f"Lphy={accum['phys']:.5f} | "
                  f"Lpas={accum['passiv']:.2e} || "
                  f"SMAPE(obs)={val_smape_obs:.3f}%  "
                  f"SMAPE(all)={val_smape_all:.3f}%  "
                  f"σ_Ld/σ_obs={ratio:.2f}×")

    model.load_state_dict(best_state)
    print(f"\n✅ Best weights restored (val_SMAPE_obs={best_val_smape:.4f}% "
          f"@ epoch {best_epoch}/{epochs})")
    return model, history, best_epoch


